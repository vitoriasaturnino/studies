=begin
 O spaceship operator ( <=> ) do Ruby tem apenas 3 possibilidades de retorno:
 
  -1: se o primeiro operando for menor que o segundo
  0: se os operandos forem iguais
  1: se o primeiro operando for maior que o segundo
  
  Exemplo:
  puts 1 <=> 2 retorna -1
  puts 1 <=> 1 retorna 0
  puts 2 <=> 1 retorna 1
=end 
